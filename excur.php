<p style="font-size:12px;">Fent una mica d’història, l’excursionisme és el primer dels esports de Muntanya que es sol practicar i dins ell tenim moltes modalitats sempre en funció del nivell de la sortida i els seus participants.</p></br>

<p style="font-size:12px;">Potser per aquesta varietat, al Club uns anys vam fer sortides familiars, altre temps férem una tracalada de passos i entre una època i l’altre hi ha un abisme difícil de gestionar perquè els interessos s’han enfocat més en la resta de modalitats esportives de muntanya com l’Escalada, la Bicicleta de Muntanya, el Trailrunning per les Curses o el Barranquisme però com a Club i amb el nostre objectiu de caràcter social i per oferir a la gent la possibilitat de realitzar excursions i gaudir de la Muntanya, ens pertoca mantenir aquesta secció en marxa… caminant i fent camí per les muntanyes!!</p></br>

<p style="font-size:12px;">Ja a principis d’any 2013 amb les renovacions, vam decidir firmar el conveni de col·laboració amb el Grup Excursionista d’Esporles que com a germans que estimen la muntanya ens brindaren el seu calendari anual de sortides per participar-hi sempre tenint en compte el seu manament únic que compartim:</p></br>

<p style="font-size:10px;"><em>“Que cap persona se n’adoni que per allà hi han passat els excursionistes esporlerins”</br>(setembre 1993)</em></p></br>

<p style="font-size:12px;">I posteriorment arrel de la presentació a Esporles del Catàleg de camins i el Visor de Camins ofert per l’IMEDEA, va sorgir la idea de compaginar les sortides del GEE que solen esser per tota la Serra amb una altre Sortida mensual a la nostre Vall i els seus voltants.</p></br>

<p style="font-size:12px;">Amb aquestes curolles i totes les que puguin anar sortint, el Club reactiva aquesta secció gràcies a tots els voluntaris col·laboradors i els acords amb entitats locals com l’Ajuntament d’Esporles i el Grup Excursionista d’Esporles i d’altres que pensem que poc a poc ens ajudaran a créixer participant en l’esport mare del Muntanyisme que considerem és l’excursionisme!!</p></br>

<p style="font-size:12px;">Animeu-vos a participar com a col·laboradors de la secció o en els calendaris programats que ens permetran passar dies a la natura inoblidables per a tots!!!</p></br>

