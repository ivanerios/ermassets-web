<p style="font-size:12px;">Degut a les peculiaritats de l’escalada, sempre ha existit un buit en les primeres etapes formatives d’aquesta. Els motius poden ser molts i variats: poca associació entre escaladors a nivell federatiu, ser un dels anomenats esports minoritaris, la necessitat d’uns mínims coneixements tècnics, l’aprenentatge d’ús de materials tècnics, etc. </p></br>
<p style="font-size:12px;">Aquest problema es va fer evident per un grup d’amics que formaven part del Club de Muntanya i Escalada Ermassets, els qui van decidir posar solució. D’aquesta manera neix l’Escola d’Escalada Ermassets a l’any 2009.</p></br>
<p style="font-size:12px;">L’escola està orientada per cobrir les necessitats formatives de les primeres categories o “esport base” (nins/es de entre 5 i 17 anys), però també volen donar la possibilitat d’apropament a totes aquelles persones amb interès per escalar, que mai han pogut o no s’han atrevit a fer-ho.</p></br>

<p style="font-size:12px;">L’escalada és una de les formes bàsiques dels moviments en els éssers humans, pel que és una activitat clau per descobrir l’entorn infantil i juvenil; afavoreix la salut gràcies a una millora de la musculatura esquelètica, de la flexibilitat, de les facultats coordinatives i psicològiques, etc; afavoreix la cooperació, la relació entre companys; fomenta el desenvolupament de la personalitat, l’autoestima, el contacte amb la naturalesa, etc.</p></br>
<p style="font-size:12px;">Han passat ja 5 anys des de la creació de l’escola d’escalada. Durant aquets, han participat més de 200 alumnes.</p></br>
<p style="font-size:12px;">Les activitats que composen l’escola són: les classes d’escalada que s’imparteixen setmanalment, adaptades a cada grup en funció de nivell i edat; el campus d’estiu; i diferents activitats competitives que es realitzen al llarg de tot l’any, com poden ser lligues d’escalada internes, jornades de portes obertes durant les festes de Sant Pere, etc…</p></br>
<p style="font-size:12px;">Després de tot aquest temps, l’escola s’ha consolidat com un dels valors més importants del Club Ermassets i com a referència a nivell comarcal de la bona implicació respecte al esport base. Com a mostra es poden indicar les diverses victòries absolutes a la Copa Balear d’escalada en bloc, per part de dos esportistes formats a l’escola, així com excel·lents resultats en els campionats i copes d’Espanya d’escalada en dificultat durant l’any 2012.</p></br>
<p style="font-size:12px;">Desitgem que això, segueixi així durant molts d’anys.</p></br>
